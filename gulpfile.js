function _css(){
  /*
   * CSS
   */
  return gulp.src("src/css/terminus.css")
    .pipe(concat('min.css'))
    .pipe(postcss(postcss_plugins));
}

function _images(){
  /*
   * Images
   */
  return gulp.src('src/img/*')
    .pipe(gulp.dest('webroot/img/'));
}
function _sounds(){
  /*
   * Sounds
   */
  return gulp.src('src/snd/*.wav')
    .pipe(gulp.dest('webroot/snd/'));
}

